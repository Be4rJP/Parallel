package be4rjp.parallel.util;

public class LightLevelInfo {
    public final int x;
    public final int y;
    public final int z;
    public final int level;
    
    public LightLevelInfo(int x, int y, int z, int level){
        this.x = x;
        this.y = y;
        this.z = z;
        this.level = level;
    }
}
